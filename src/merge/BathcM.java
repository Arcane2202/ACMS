    private void batchTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_batchTableMouseClicked
        // TODO add your handling code here:
        mod = (DefaultTableModel) batchTable.getModel();
        rowIndx = batchTable.getSelectedRow();
        batchId.setText(mod.getValueAt(rowIndx, 0).toString());
        batchName.setText(mod.getValueAt(rowIndx, 1).toString());
        String corSelect = mod.getValueAt(rowIndx, 2).toString();
        for (int i = 0; i < course.getItemCount(); i++) {
            if (course.getItemAt(i).toString().equals(corSelect)) {
                course.setSelectedIndex(i);
                break;
            }
        }
    }//GEN-LAST:event_batchTableMouseClicked

