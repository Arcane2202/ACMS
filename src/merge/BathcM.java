    public void populateTable() {
        try {
            String quer = "SELECT batch_id,batch_name,course_name FROM BATCHS B INNER JOIN COURSES C ON C.course_id=B.course_id";
            ResultSet resultSet;
            PreparedStatement statement = con.prepareStatement(quer);
            resultSet = statement.executeQuery();
            batchTable.setModel(DbUtils.resultSetToTableModel(resultSet));
            searchRecordApprove("");
        } catch (SQLException ex) {
            Logger.getLogger(studentLayout.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

