    private void saveBatchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBatchActionPerformed
        // TODO add your handling code here:
        String id = batchId.getText();
        String name = batchName.getText().toString();
        String cor = course.getSelectedItem().toString();
        String corid = "";

        try {
            // Your database connections 
            Statement st = con.createStatement();

            String quer = "SELECT * FROM COURSES WHERE course_name=?";
            PreparedStatement statement = con.prepareStatement(quer);
            statement.setString(1, cor);
            ResultSet rs = statement.executeQuery();
            while (rs.next()) {
                corid = rs.getString(1);
            }
            //con.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e);
        }

        if (task.equals("Edit")) {
            try {
                String quer = "UPDATE BATCHS set batch_name=?,course_id=? WHERE batch_id=?";
                PreparedStatement statement = con.prepareStatement(quer);
                statement.setString(1, name);
                statement.setString(2, corid);
                statement.setString(3, id);
                statement.executeUpdate();
                populateTable();
                JOptionPane.showMessageDialog(this, "Successfully Updated");
                editBatch.setVisible(true);
                addBatch.setVisible(true);
                returnBatch.setVisible(true);
                saveBatch.setVisible(false);
                cancelBatch.setVisible(false);
                batchId.setText("");
                batchName.setText("");
                course.setSelectedIndex(0);
                batchId.setVisible(true);
                idlab.setVisible(true);

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex);
            }
        } else {

            try {
                String quer = "INSERT INTO BATCHS VALUES(?,?)";
                PreparedStatement statement = con.prepareStatement(quer);
                statement.setString(1, name);
                statement.setString(2, corid);
                statement.executeUpdate();
                populateTable();
                JOptionPane.showMessageDialog(this, "Successfully Added");
                editBatch.setVisible(true);
                addBatch.setVisible(true);
                returnBatch.setVisible(true);
                saveBatch.setVisible(false);
                cancelBatch.setVisible(false);
                batchId.setText("");
                batchName.setText("");
                course.setSelectedIndex(0);
                batchId.setVisible(true);
                idlab.setVisible(true);

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex);
            }
        }
    }//GEN-LAST:event_saveBatchActionPerformed

